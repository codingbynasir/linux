#Change permission of directory in linux
      >sudo chmod 777 /opt/lampp/htdocs
      
here 777 means, you can read write any file or folder.
If you wish to not write, simply use 700 instead of 777 and change the directory to your directory

#To install java8 in linux 
      > sudo add-apt-repository ppa:webupd8team/java
      > sudo apt-get update
      > sudo apt-get install oracle-java7-installer
#Set environment variable for java8
      > sudo apt-get install oracle-java8-set-default
#Remove java from linux distro
      >sudo apt-get remove oracle-java8-installer

#Download Youtube single video or a full play list
      step 1:
            Open the command >sudo apt-get install youtube-dl
      step 2:
            Now copy the youtube video url or playlist url
      step 3:
            Type >youtube-dl youtube url
            *Replace the youtube url with your copied url from clipboard
      Step 4:
            Hit the enter button and start the downloading
